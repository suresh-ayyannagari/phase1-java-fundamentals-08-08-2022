package com.simplilearn.typecasting;

public class SuperHero {

	// protected data
	protected int rank =100;
	protected String name="Thor";
	protected String power="Flying Hamer";
	
	
	// protected methods
	protected void showHero() {
		System.out.println("Rank : "+rank);
		System.out.println("Name : "+name);
		System.out.println("Pwer : "+power);
	}
	
}
